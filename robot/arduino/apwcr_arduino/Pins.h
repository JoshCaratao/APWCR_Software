/*
  Pins.h

  Purpose:
  Central location for all Arduino pin assignments for the APWCR robot. This file prevents pin numbers
  from being scattered throughout the code and makes rewiring or board changes easy.

  What belongs here:
  - Motor driver pins (PWM and direction pins for each DC motor)
  - Encoder pins (channel A and channel B for each encoder)
  - Ultrasonic pins (TRIG and ECHO)
  - Servo signal pins
*/